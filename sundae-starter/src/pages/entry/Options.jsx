import axios from "axios";
import { useEffect, useState } from "react";
import ScoopOption from "./ScoopOption.jsx";
import { Row } from "react-bootstrap";
import ToppingOption from "./ToopingOption.jsx";
import AlertBanner from "../common/AlertBanner.jsx";
import { pricePerItem } from "../../constants/index.js";
import { formatCurrency } from "../../utils/index.js";
import { useOrderDetails } from "../../contexts/OrderDetails.jsx";

export default function Options({ optionType }) {
  const [items, setItems] = useState([]);
  const [error, setError] = useState(false);
  const { totals } = useOrderDetails();

  useEffect(() => {
    axios
      .get(`http://localhost:3030/${optionType}`)
      .then((response) => setItems(response.data))
      .catch((error) => setError(true));
  }, [optionType]);

  if (error) {
    return <AlertBanner />;
  }

  const ItemComponent = optionType === "scoops" ? ScoopOption : ToppingOption;
  const title = optionType[0].toUpperCase() + optionType.slice(1);

  const optionItems = items.map((item) => (
    <ItemComponent
      key={item.name}
      name={item.name}
      imagePath={item.imagePath}
    />
  ));

  return (
    <>
      <h2>{title}</h2>
      <p>{formatCurrency(pricePerItem[optionType])}</p>
      <p>
        {title} total: {formatCurrency(totals[optionType])}
      </p>
      <Row>{optionItems}</Row>
    </>
  );
}
